CREATE  FUNCTION fnTraceAssembleMatName
	(@FIRSTPARTOFCOND	INT , @SECONDPARTOFCOND		NVARCHAR(200)  )
	
	RETURNS @RESULT TABLE(NUMBER [INT],[GUID] UNIQUEIDENTIFIER , [CODE] NVARCHAR(200) COLLATE ARABIC_CI_AI , [NAME] NVARCHAR(200) COLLATE ARABIC_CI_AI)
	
	AS
	BEGIN
		IF(@FIRSTPARTOFCOND = 0 )
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 
		ELSE IF (@FIRSTPARTOFCOND = 1)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE Dim = '' + @SECONDPARTOFCOND + ''
		ELSE IF (@FIRSTPARTOFCOND = 2)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE Origin = '' + @SECONDPARTOFCOND + ''
		ELSE IF (@FIRSTPARTOFCOND = 3)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE Pos = '' + @SECONDPARTOFCOND + ''
		ELSE IF (@FIRSTPARTOFCOND = 4)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE Company = '' + @SECONDPARTOFCOND + ''
		ELSE IF (@FIRSTPARTOFCOND = 5)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE Color = '' + @SECONDPARTOFCOND + ''
		ELSE IF (@FIRSTPARTOFCOND = 6)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE Provenance = '' + @SECONDPARTOFCOND + ''
		ELSE IF (@FIRSTPARTOFCOND = 7)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE Quality = '' + @SECONDPARTOFCOND + ''
		ELSE IF (@FIRSTPARTOFCOND = 8)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE Model = '' + @SECONDPARTOFCOND + ''
		ELSE IF (@FIRSTPARTOFCOND = 9)
			INSERT INTO @RESULT SELECT NUMBER,GUID, CODE, [NAME] FROM MT000 WHERE GroupGUID = '' + @SECONDPARTOFCOND + ''
		RETURN
	END
