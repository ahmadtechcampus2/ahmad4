CREATE PROCEDURE prcPOSSD_Order_GetCustomerTableFldsName
	@isRTL          [BIT]
AS
/*******************************************************************************************************
Get Customer fields name 
*******************************************************************************************************/
	
	SELECT 
	        Number AS Id,
	        Asc1   AS Name
	FROM mc000 
	WHERE Number IN (1555, 1556, 1557, 603, 1558, 1559, 1560, 604, 1304, 1561, 1562, 1306, 1307, 1305, 6908, 6909, 6910, 6911)
